/**
 * Holds the interface required for an Action of
 * the robot.
 * @author 
 *
 */
public interface Action {
	/**
	 * An action method is needed for a method to implement
	 * an Action interface.
	 */
    void action();
    /**
     * A suppress method is needed for a method to implement
     * an Action method.
     *
     */
    void suppress();
}
